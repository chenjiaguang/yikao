let years = []
let months = []
const nowYear = new Date().getFullYear()
const nowMonth = new Date().getMonth()
for (let i = 0; i < 100; i++) {
  years.unshift({
    value: (nowYear - i).toString(),
    text: (nowYear - i).toString() + '年'
  })
}
for (let j = 0; j < 12; j++) {
  months.push({
    value: (j + 1).toString(),
    text: (j + 1).toString() + '月'
  })
}

